import dec2roman as mod

while True:
    print('To exit the program, please type "exit")')
    number = input("Please enter a number between 1 and 3999, inclusively :")
    
    if number.lower() == "exit":
        print("Exiting the program... Good Bye")
        break
    elif number.isdigit() == True:
        if int(number)>3999 or int(number)<1:
            print("Not Valid Input !!!")
            continue
        else:
            print(mod.dec2roman(int(number)))
            break
    elif type(number) == str:
        print("Not Valid Input !!!")
        continue


